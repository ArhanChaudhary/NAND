import assert from "assert";
import { PC, tick, tock } from "../build/release.js";
export function TestRAM16K() {
tick();
assert.strictEqual(PC(0, false, false), 0);
tock();
assert.strictEqual(PC(0, false, false), 1);
tick();
assert.strictEqual(PC(33413, false, false), 1);
tock();
assert.strictEqual(PC(33413, false, false), 2);
tick();
assert.strictEqual(PC(33413, true, false), 2);
tock();
assert.strictEqual(PC(33413, true, false), 33413);
tick();
assert.strictEqual(PC(33413, false, false), 33413);
tock();
assert.strictEqual(PC(33413, false, false), 33414);
tick();
assert.strictEqual(PC(33413, false, false), 33414);
tock();
assert.strictEqual(PC(33413, false, false), 33415);
tick();
assert.strictEqual(PC(12345, true, true), 33415);
tock();
assert.strictEqual(PC(12345, true, true), 0);
tick();
assert.strictEqual(PC(12345, true, false), 0);
tock();
assert.strictEqual(PC(12345, true, false), 12345);
tick();
assert.strictEqual(PC(12345, true, true), 12345);
tock();
assert.strictEqual(PC(12345, true, true), 0);
tick();
assert.strictEqual(PC(12345, false, false), 0);
tock();
assert.strictEqual(PC(12345, false, false), 1);
tick();
assert.strictEqual(PC(12345, false, true), 1);
tock();
assert.strictEqual(PC(12345, false, true), 0);
tick();
assert.strictEqual(PC(0, true, false), 0);
tock();
assert.strictEqual(PC(0, true, false), 0);
tick();
assert.strictEqual(PC(0, false, false), 0);
tock();
assert.strictEqual(PC(0, false, false), 1);
console.log("RAM16K test cases passed");
}